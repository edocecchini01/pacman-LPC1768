void count_down()
{
	uint32_t count = 1 * 25000000; //1 secondo
	init_timer(1, 0, count);
	enable_timer(1);
}

void tim_PacMan(uint32_t speed) //in milliseconds
{
	uint32_t count = (speed * 25000000)/1000;
	init_timer(0, 0, count);
	enable_timer(0);
}

