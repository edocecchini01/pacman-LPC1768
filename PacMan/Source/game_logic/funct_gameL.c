void direct_pacMan(Direction direction)
{
	switch(direction){
		case LEFT:
			move_pacMan(gs, -7, 0);
			gs.posPac_X = gs.posPac_X + -7;
			break;
		case RIGHT:
			move_pacMan(gs, 7, 0);
			gs.posPac_X = gs.posPac_X + 7;
			break;
		case UP:
			move_pacMan(gs, 0, -7);
			gs.posPac_Y = gs.posPac_Y + -7;
			break;
		case DOWN:
			move_pacMan(gs, 0, 7);
			gs.posPac_Y = gs.posPac_Y + 7;
			break;
		default:
			break;
	}
}

